/***
 * • Escreva um algoritmo que some os números entre 1 e 10,
 * escrevendo o resultado final;
 */
public class ForQuestao1 {
    public static void main(String[] args) {
        int soma = 0;
        for (int i = 1; i <= 10; i++) {
            soma += i;
        }
        System.out.println(soma);
    }
}
